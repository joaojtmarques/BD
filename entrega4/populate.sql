insert into d_utilizador(email, tipo)
    select email, 'qualificado' from utilizador_qualificado natural join utilizador;

insert into d_utilizador(email, tipo)
    select email, 'regular' from utilizador_regular natural join utilizador;

insert into d_tempo(dia, dia_da_semana, semana, mes, trimestre, ano)
    select extract(day from ts) as dia,
    extract (DOW from ts) as dia_da_semana,
    extract (WEEK from ts) as semana,
    extract(month from ts) as mes,
    extract (QUARTER from ts) as trimestre,
    extract(year from ts) as ano
    from anomalia
    order by dia, dia_da_semana, semana, mes, trimestre, ano;


insert into d_local(latitude, longitude, nome) 
    select latitude, longitude, nome from local_publico;

insert into d_lingua(lingua)
    select lingua from anomalia;



insert into f_anomalia(id_utilizador, id_tempo, id_local, id_lingua, tipo_anomalia, com_proposta)
    select id_utilizador, id_tempo, id_local, id_lingua, tipo_anomalia, com_proposta 
    from 
    	((select id_utilizador, id_tempo, id_local, id_lingua, 'redacao' as tipo_anomalia 
    	from incidencia natural join d_utilizador 
    	inner join (item natural join d_local) on incidencia.item_id = item.id 
    	inner join anomalia X on incidencia.anomalia_id = X.id natural join d_lingua 
    	inner join d_tempo Y on (extract(day from X.ts) = Y.dia 
    	and extract(DOW from  X.ts) = Y.dia_da_semana 
    	AND extract(WEEK from X.ts)= Y.semana AND extract(month from X.ts)= Y.mes 
    	AND extract(QUARTER from X.ts)= Y.trimestre 
    	and extract(year from X.ts) = Y.ano) 
    	where tem_anomalia_redacao = true 
    	group by id_utilizador, id_tempo, id_local, id_lingua, tem_anomalia_redacao 
    	union all
    	select id_utilizador, id_tempo, id_local, id_lingua, 'traducao' as tipo_anomalia  
    	from incidencia natural join d_utilizador 
    	inner join (item natural join d_local) on incidencia.item_id = item.id 
    	inner join anomalia X on incidencia.anomalia_id = X.id 
    	natural join d_lingua inner join d_tempo Y on (extract(day from X.ts) = Y.dia 
		and extract(DOW from  X.ts) = Y.dia_da_semana 
		AND extract(WEEK from X.ts)= Y.semana 
		AND extract(month from X.ts)= Y.mes 
		AND extract(QUARTER from X.ts)= Y.trimestre 
		and extract(year from X.ts) = Y.ano) 
		where tem_anomalia_redacao = false 
		group by id_utilizador, id_tempo, id_local, id_lingua, tem_anomalia_redacao ) as fee 
	natural join 
		(select id_utilizador, id_tempo, id_local, id_lingua, true as com_proposta 
		from incidencia natural join d_utilizador 
		inner join (item natural join d_local) on incidencia.item_id = item.id 
		inner join anomalia X on incidencia.anomalia_id = X.id 
		natural join d_lingua inner join d_tempo Y on (extract(day from X.ts) = Y.dia 
			and extract(DOW from  X.ts) = Y.dia_da_semana 
			AND extract(WEEK from X.ts)= Y.semana 
			AND extract(month from X.ts)= Y.mes 
			AND extract(QUARTER from X.ts)= Y.trimestre 
			and extract(year from X.ts) = Y.ano) 
			inner join correcao on correcao.anomalia_id = X.id 
			group by id_utilizador, id_tempo, id_local, id_lingua 
			union all 
			select id_utilizador, id_tempo, id_local, id_lingua, false as com_proposta 
			from incidencia natural join d_utilizador 
			inner join (item natural join d_local) on incidencia.item_id = item.id 
			inner join anomalia X on incidencia.anomalia_id = X.id natural join d_lingua 
			inner join d_tempo Y on (extract(day from X.ts) = Y.dia 
			and extract(DOW from  X.ts) = Y.dia_da_semana 
			AND extract(WEEK from X.ts)= Y.semana 
			AND extract(month from X.ts)= Y.mes 
			AND extract(QUARTER from X.ts)= Y.trimestre 
			and extract(year from X.ts) = Y.ano) 
			full OUTER join correcao on correcao.anomalia_id = X.id 
			where correcao.anomalia_id IS null or X.id IS null 
			group by id_utilizador, id_tempo, id_local, id_lingua) as foo) as fii;



